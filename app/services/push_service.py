async def _delete_stale_subscriptions(db: AsyncSession, endpoints: list[str]) -> None:
    if not endpoints:
        return
    for ep in endpoints:
        sub = (
            await db.execute(
                select(PushSubscription).where(PushSubscription.endpoint == ep)
            )
        ).scalars().first()
        if sub:
            await db.delete(sub)
    await db.commit()


