async def send_push_to_users(
    db: AsyncSession,
    user_ids: Sequence[UUID],
    title: str,
    body: str,
    url: str = "/",
    icon: str = "/static/icons/icon-192.png",
) -> None:
    """
    1. Persist notification to DB inbox for each user.
    2. Store the payload in-memory so the SW can fetch it after wakeup.
    3. Send an empty push to every subscription belonging to the given user_ids.
    4. Clean up stale (410/404) subscriptions from the DB.
    """
    if not user_ids:
        return

    subscriptions = (
        await db.execute(
            select(PushSubscription).where(PushSubscription.user_id.in_(user_ids))
        )
    ).scalars().all()

    # Persist to inbox DB + in-memory pending store for each user
    for uid in user_ids:
        await store_pending_notification(uid, title=title, body=body, url=url, icon=icon, db=db)

    if not subscriptions:
        return

    stale_endpoints: list[str] = []

    async def _push(sub: PushSubscription) -> None:
        ok = await asyncio.to_thread(_send_empty_push_sync, sub.endpoint)
        if not ok:
            stale_endpoints.append(sub.endpoint)

    await asyncio.gather(*[_push(sub) for sub in subscriptions])
    await _delete_stale_subscriptions(db, stale_endpoints)


