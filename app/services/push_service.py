logger = logging.getLogger(__name__)
ENV_PATH = Path(__file__).resolve().parents[2] / ".env"


class PushConfigurationError(RuntimeError):
    """Raised when local Web Push/VAPID configuration is invalid."""

# ---------------------------------------------------------------------------
# Notification payloads are persisted so the web process and worker process
# can communicate through the database instead of in-memory state.
# ---------------------------------------------------------------------------


async def store_pending_notification(
    user_id: UUID, title: str, body: str, url: str = "/", icon: str = "/static/icons/icon-192.png",
    db: AsyncSession | None = None,
) -> None:
    """Persist a notification for the inbox and service-worker payload fetch."""
    if db is not None:
        try:
            notif = Notification(
                user_id=user_id, title=title, body=body, url=url, icon=icon
            )
            db.add(notif)
            await db.commit()
        except Exception:
            logger.exception("Failed to persist notification to DB for user %s", user_id)


# ---------------------------------------------------------------------------
# VAPID helpers (pure Python via ecdsa + stdlib)
# ---------------------------------------------------------------------------

def _b64url_encode(data: bytes) -> str:
    return base64.urlsafe_b64encode(data).rstrip(b"=").decode()


def _b64url_decode(s: str) -> bytes:
    pad = "=" * (4 - len(s) % 4) if len(s) % 4 else ""
    try:
        return base64.urlsafe_b64decode(s + pad)
    except (binascii.Error, ValueError) as exc:
        raise PushConfigurationError("Invalid VAPID_PRIVATE_KEY base64url encoding.") from exc


def _load_signing_key(private_key_raw: str) -> _ecdsa.SigningKey:
    """
    Load VAPID private key from:
      - base64url-encoded raw 32-byte EC scalar (generated by py-vapid / vapid-gen)
      - base64url-encoded DER EC private key structure (121 bytes, generated by web-push libraries)
      - PEM string (-----BEGIN EC PRIVATE KEY-----)
    """
    s = private_key_raw.strip()
    if s.startswith("-----"):
        return _ecdsa.SigningKey.from_pem(s, hashfunc=hashlib.sha256)
    # Raw base64url-encoded bytes
    raw = _b64url_decode(s)
    if len(raw) == 32:
        return _ecdsa.SigningKey.from_string(raw, curve=_ecdsa.NIST256p, hashfunc=hashlib.sha256)
    if len(raw) == 121:
        return _ecdsa.SigningKey.from_der(raw, hashfunc=hashlib.sha256)
    raise PushConfigurationError(
        f"Invalid VAPID_PRIVATE_KEY length after decoding: {len(raw)} bytes."
    )


def _make_vapid_headers(endpoint: str, private_key_str: str, email: str) -> dict:
    """
    Build VAPID Authorization + encoded public key headers for a push request.

    Returns a dict with:
      Authorization: vapid t=<jwt>,k=<pubkey_b64url>
      TTL: 86400
    """
    parsed = urlparse(endpoint)
    audience = f"{parsed.scheme}://{parsed.netloc}"

    sk = _load_signing_key(private_key_str)
    vk = sk.get_verifying_key()

    # Uncompressed EC public key: 0x04 || x (32 bytes) || y (32 bytes) = 65 bytes
    pub_bytes = b"\x04" + vk.to_string()
    pub_b64 = _b64url_encode(pub_bytes)

    # Build JWT header + claims
    jwt_header = _b64url_encode(json.dumps({"typ": "JWT", "alg": "ES256"}).encode())
    jwt_claims = _b64url_encode(
        json.dumps(
            {"aud": audience, "exp": int(time.time()) + 43200, "sub": email}
        ).encode()
    )
    signing_input = f"{jwt_header}.{jwt_claims}".encode()

    # Sign with ES256 (ECDSA P-256 + SHA-256), output raw r||s (64 bytes)
    sig = sk.sign(
        signing_input,
        hashfunc=hashlib.sha256,
        sigencode=_ecdsa_util.sigencode_string,
    )
    jwt_token = f"{jwt_header}.{jwt_claims}.{_b64url_encode(sig)}"

    return {
        "Authorization": f"vapid t={jwt_token},k={pub_b64}",
        "TTL": "86400",
        "Content-Length": "0",
    }


def _push_env() -> tuple[str, str]:
    """Load VAPID private key + claims email from .env (lazy, cached by dotenv)."""
    load_dotenv(ENV_PATH, override=False)
    private_key = os.getenv("VAPID_PRIVATE_KEY", "").strip()
    claims_email = os.getenv("VAPID_CLAIMS_EMAIL", "mailto:admin@example.com").strip()
    return private_key, claims_email


# ---------------------------------------------------------------------------
# Core send — empty push (no encrypted payload)
# ---------------------------------------------------------------------------

def _send_empty_push_sync(endpoint: str) -> bool:
    """
    Send an empty (no-payload) VAPID push to one subscription endpoint.
    Returns True  → push delivered or transient error (keep subscription)
    Returns False → subscription expired/gone (410/404 → delete from DB)
    """
    private_key, claims_email = _push_env()
    if not private_key:
        logger.warning("VAPID_PRIVATE_KEY not configured; skipping push.")
        return True

    try:
        headers = _make_vapid_headers(endpoint, private_key, claims_email)
        resp = httpx.post(endpoint, headers=headers, timeout=10.0)

        if resp.status_code in (404, 410):
            logger.info("Stale subscription removed: %s…", endpoint[:50])
            return False  # Caller will delete from DB

        if resp.status_code >= 400:
            logger.warning("Push endpoint returned %s for %s…", resp.status_code, endpoint[:50])

        return True

    except PushConfigurationError:
        raise
    except Exception as exc:
        logger.warning("Push send error for %s…: %s", endpoint[:50], exc)
        return True  # Transient — keep subscription


